package vo;

public class Menu {
	private int id ;
	
	private String menuName ;
	
	private int menuShowOrder ;
	
	private int enable ;
	
	
	
	public int getEnable() {
		return enable;
	}

	public void setEnable(int enable) {
		this.enable = enable;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getMenuName() {
		return menuName;
	}

	public void setMenuName(String menuName) {
		this.menuName = menuName;
	}

	public int getMenuShowOrder() {
		return menuShowOrder;
	}

	public void setMenuShowOrder(int menuShowOrder) {
		this.menuShowOrder = menuShowOrder;
	}


}
